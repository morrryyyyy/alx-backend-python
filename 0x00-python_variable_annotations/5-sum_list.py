#!/usr/bin/env python3
'''5-sum_list.py'''
from typing import List


def sum_list(input_list: List[float]) -> float:
    '''
    Adds the elements  of a list.

    Args:
        input_list(Vector): the list of floats.

    Returns:
        sum(float): the sum of the elements.
    '''
    return sum(input_list)

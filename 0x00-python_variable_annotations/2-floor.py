#!/usr/bin/env python3
'''2-floor: Function to return the largest integer less than or equal to a float.'''
import math

def floor(n: float) -> int:
    """Returns the floor of a float value.

    Args:
        n (float): The input float value.

    Returns:
        int: The largest integer less than or equal to the input float.
    """
    return math.floor(n)

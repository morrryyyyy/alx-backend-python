from typing import List, Union


def sum_mxd_list(mxd_list: List[Union[float, int]]) -> float:
    '''
    Adds the elements  of a list.

    Args:
        mxd_list(Vector): the list of floats.

    Returns:
        sum(float): the sum of the elements.
    '''
    return sum(mxd_list)
